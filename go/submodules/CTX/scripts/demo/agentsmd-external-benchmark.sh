#!/usr/bin/env bash
set -euo pipefail

ROOT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")/../.." && pwd)"
INPUT_DIR="$ROOT_DIR/benchmarks/external/agentsmd"
OUTPUT_DIR="${CTX_EXTERNAL_BENCHMARK_OUTPUT_DIR:-$INPUT_DIR}"
CTX_BIN="${1:-$ROOT_DIR/target/debug/ctx}"
REPO_URL="${CTX_EXTERNAL_BENCHMARK_REPO_URL:-https://github.com/agentsmd/agents.md.git}"
REPO_NAME="${CTX_EXTERNAL_BENCHMARK_REPO_NAME:-agentsmd}"
PYTHON_BIN="${PYTHON_BIN:-$(command -v python3 || command -v python || true)}"

if [[ -z "$PYTHON_BIN" ]]; then
  echo "python3 or python is required to render the benchmark summary" >&2
  exit 1
fi

if [[ "$CTX_BIN" != /* ]]; then
  CTX_BIN="$ROOT_DIR/$CTX_BIN"
fi

TMP_ROOT="$(mktemp -d)"
trap 'rm -rf "$TMP_ROOT"' EXIT
EXTERNAL_REPO="$TMP_ROOT/$REPO_NAME"
SPEC_PATH="$TMP_ROOT/ctx-memory-suite.toml"

mkdir -p "$OUTPUT_DIR"

git clone --depth 1 "$REPO_URL" "$EXTERNAL_REPO" >/dev/null

cat > "$SPEC_PATH" <<EOF
title = "CTX External Benchmark: agentsmd/agents.md"

[[cases]]
name = "dev_server_guidance"
query = "npm run dev npm run build lockfile development server"
markdown = "$EXTERNAL_REPO/AGENTS.md"
limit = 20
checklist = "$INPUT_DIR/checklist.md"
markdown_answer = "$INPUT_DIR/markdown-answer.txt"
graph_answer = "$INPUT_DIR/graph-answer.txt"
EOF

"$CTX_BIN" --repo-root "$EXTERNAL_REPO" init >/dev/null
"$CTX_BIN" --repo-root "$EXTERNAL_REPO" memory import \
  --from "$EXTERNAL_REPO/AGENTS.md" \
  --scope project \
  --source markdown \
  --prefix agents >/dev/null

"$CTX_BIN" --repo-root "$EXTERNAL_REPO" benchmark memory-suite \
  --spec "$SPEC_PATH" \
  --report-out "$OUTPUT_DIR/report.md" \
  --json-out "$OUTPUT_DIR/report.json" >/dev/null

"$PYTHON_BIN" - "$OUTPUT_DIR/report.json" <<'PY'
import json
import sys
from pathlib import Path

report_path = Path(sys.argv[1])
data = json.loads(report_path.read_text())
data["spec_path"] = "generated by scripts/demo/agentsmd-external-benchmark.sh"
data["report_markdown_path"] = "benchmarks/external/agentsmd/report.md"
data["json_output_path"] = "benchmarks/external/agentsmd/report.json"

for case in data.get("cases", []):
    result = case.get("result", {})
    result["markdown_path"] = "AGENTS.md (cloned from https://github.com/agentsmd/agents.md)"

report_path.write_text(json.dumps(data, indent=2) + "\n")
PY

grep 'CTX External Benchmark: agentsmd/agents.md' "$OUTPUT_DIR/report.md" >/dev/null
grep '"graph_quality_wins": 1' "$OUTPUT_DIR/report.json" >/dev/null

echo "CTX external benchmark passed: $REPO_URL"
echo "Report markdown: $OUTPUT_DIR/report.md"
echo "Report JSON: $OUTPUT_DIR/report.json"
