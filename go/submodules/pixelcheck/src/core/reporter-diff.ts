type DeltaKind = "score" | "issues" | "cost" | "duration";

/**
 * Markdown delta arrow. "score" goes up = good (▲); "issues"/"cost"/
 * "duration" go up = bad (▲ red). Returns plain Markdown text — no
 * HTML, no inline styles, GFM-portable.
 */
function arrowMd(delta: number, kind: DeltaKind, unit = ""): string {
  if (Math.abs(delta) < 1e-6 || (kind === "issues" && delta === 0)) {
    return "—";
  }
  const isImprovement =
    kind === "score" ? delta > 0 : delta < 0;
  const arrow = delta > 0 ? "▲" : "▼";
  const formatted = formatDelta(delta, kind, unit);
  // GFM doesn't honour <span style>; we encode polarity via inline
  // text labels [OK] / [WARN] (CLAUDE.md 铁律 #10 — 通知中禁止 emoji；
  // PR diff comments are notification channel posted into GitHub PRs).
  const polarity = isImprovement ? "[OK]" : "[WARN]";
  return `${arrow} ${formatted} ${polarity}`;
}

function formatDelta(delta: number, kind: DeltaKind, unit: string): string {
  const sign = delta > 0 ? "+" : "";
  if (kind === "issues") {
    return `${sign}${Math.round(delta)}${unit}`;
  }
  if (kind === "cost") {
    return `${delta < 0 ? "-" : "+"}$${Math.abs(delta).toFixed(3)}`;
  }
  // score / duration / generic — 1 decimal
  return `${sign}${delta.toFixed(1)}${unit}`;
}

/**
 * HTML delta arrow with inline colour. Wrapped in a span carrying
 * a `delta-{up|down|flat}` class for stylesheet targeting.
 */
function arrowHtml(delta: number, kind: DeltaKind, unit = ""): string {
  if (Math.abs(delta) < 1e-6 || (kind === "issues" && delta === 0)) {
    return `<span class="delta flat">—</span>`;
  }
  const isImprovement =
    kind === "score" ? delta > 0 : delta < 0;
  const cls = isImprovement ? "up" : "down";
  const arrow = delta > 0 ? "▲" : "▼";
  return `<span class="delta ${cls}">${arrow} ${formatDelta(delta, kind, unit)}</span>`;
}

function formatFromExtension(filePath: string): DiffReportFormat {
  const ext = path.extname(filePath).toLowerCase();
  if (ext === ".md" || ext === ".markdown") return "markdown";
  if (ext === ".html" || ext === ".htm") return "html";
  if (ext === ".json") return "json";
  return "text";
}

